function initMenuToggle(){
    $('#menu_toggle').click(function(){
        menuToggle(); 
    });
}

function menuToggle(){
    $MAIN_CONTAINER = $('#main-container');
    if ($MAIN_CONTAINER.hasClass('nav-md')) {
            $SIDEBAR_MENU.find('li.active ul').hide();
            $SIDEBAR_MENU.find('li.active').addClass('active-sm').removeClass('active');
        } else {
            $SIDEBAR_MENU.find('li.active-sm ul').show();
            $SIDEBAR_MENU.find('li.active-sm').addClass('active').removeClass('active-sm');
        }

        $MAIN_CONTAINER.toggleClass('nav-md nav-sm');

        setContentHeight();
}

function initItemsInMenu(){
    $('.sidebar-item').click(function(){
        showItemSidebarMenu(this);
    });
}

/**
 * Add the active class in the current item of the left menu
 * @returns {undefined}
 */
function initCurrentItemInMenu(){
        $SIDEBAR_MENU.find('a[href="' + CURRENT_URL + '"]').parent('li').addClass('current-page');

        $SIDEBAR_MENU.find('a').filter(function () {
            return this.href == CURRENT_URL;
        }).parent('li').addClass('current-page').parents('ul').slideDown(function() {
            setContentHeight();
        }).parent().addClass('active');
}


// Sidebar Menu
function showItemSidebarMenu(element)
{
    var $li = $(element).parent();
    if ($li.is('.active')) {
        $li.removeClass('active active-sm');
        $('ul:first', $li).slideUp(function() {
            setContentHeight();
        });
    } else {
        // prevent closing menu if we are on child menu
        if (!$li.parent().is('.child_menu')) {
            $SIDEBAR_MENU.find('li').removeClass('active active-sm');
            $SIDEBAR_MENU.find('li ul').slideUp();
        }

        $li.addClass('active');
        
        //Close previous item
        if ($PREVIOUS_ITEM !== null){
                $PREVIOUS_ITEM.removeClass('active');
                 $('ul:first', $PREVIOUS_ITEM).slideUp(function() {     
                     setContentHeight();
                 });
            }
            $PREVIOUS_ITEM = $li;

        $('ul:first', $li).slideDown(function() {
            setContentHeight();
        });
        
    }
}


function initAccordion(){
        $('.collapse-link').on('click', function() {
        var $BOX_PANEL = $(this).closest('.x_panel'),
            $ICON = $(this).find('i'),
            $BOX_CONTENT = $BOX_PANEL.find('.x_content');
        
        // fix for some div with hardcoded fix class
        if ($BOX_PANEL.attr('style')) {
            $BOX_CONTENT.slideToggle(200, function(){
                $BOX_PANEL.removeAttr('style');
            });
        } else {
            $BOX_CONTENT.slideToggle(200); 
            $BOX_PANEL.css('height', 'auto');  
        }

        $ICON.toggleClass('fa-chevron-up fa-chevron-down');
    });
    
    $('.close-link').click(function () {
        var $BOX_PANEL = $(this).closest('.x_panel');

        $BOX_PANEL.remove();
    });
}

